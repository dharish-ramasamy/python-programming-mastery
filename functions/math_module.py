import math

num1 = 10.4
num2 = 11.3
num3 = -10
num4 = 5
list1 = [11, 20, 30, 8, 50]
num5 = 64

#ceil()
print("Ceil", math.ceil(num1))

#floor()
print("Floor", math.floor(num2))

#fabs()
print("Fabs", math.fabs(num3))

#factorial()
print("Factorial", math.factorial(num4)) # 5 * 4 * 3 * 2 * 1

#fsum()
print("Fsum", math.fsum(list1))

#sqrt()
print("Sqrt", math.sqrt(num5))

